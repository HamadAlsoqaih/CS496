const state = {
  activeModule: localStorage.getItem('cs496ActiveModule') || 'overview',
  completed: JSON.parse(localStorage.getItem('cs496StudyProgressV1') || '{}'),
  dark: localStorage.getItem('cs496Theme') === 'dark'
};

const app = document.getElementById('app');
const nav = document.getElementById('topNav');
const overallText = document.getElementById('overallProgressText');
const overallBar = document.getElementById('overallProgressBar');
const moduleProgressList = document.getElementById('moduleProgressList');
const themeToggle = document.getElementById('themeToggle');
const resetProgress = document.getElementById('resetProgress');
const dialog = document.getElementById('imageDialog');
const dialogImage = document.getElementById('dialogImage');
const dialogCaption = document.getElementById('dialogCaption');
const closeDialog = document.getElementById('closeDialog');

function saveProgress() {
  localStorage.setItem('cs496StudyProgressV1', JSON.stringify(state.completed));
}

function allConcepts(module = null) {
  const modules = module ? [module] : courseData.modules;
  return modules.flatMap(m => m.lessons.flatMap(l => l.concepts || []));
}

function progressFor(module = null) {
  const concepts = allConcepts(module);
  if (!concepts.length) return { done: 0, total: 0, percent: 0 };
  const done = concepts.filter(c => state.completed[c.id]).length;
  return { done, total: concepts.length, percent: Math.round((done / concepts.length) * 100) };
}

function renderNav() {
  nav.innerHTML = '';
  courseData.modules.forEach(module => {
    const btn = document.createElement('button');
    btn.type = 'button';
    btn.className = 'nav-btn' + (module.id === state.activeModule ? ' active' : '');
    btn.textContent = module.nav;
    btn.addEventListener('click', () => {
      state.activeModule = module.id;
      localStorage.setItem('cs496ActiveModule', module.id);
      render();
      window.scrollTo({ top: 0, behavior: 'smooth' });
    });
    nav.appendChild(btn);
  });
}

function renderProgress() {
  const overall = progressFor();
  overallText.textContent = `${overall.percent}% (${overall.done}/${overall.total})`;
  overallBar.style.width = `${overall.percent}%`;
  moduleProgressList.innerHTML = '';
  courseData.modules.forEach(module => {
    const p = progressFor(module);
    const pill = document.createElement('div');
    pill.className = 'module-pill';
    pill.innerHTML = `<strong>${module.nav}</strong><span>${p.percent}% · ${p.done}/${p.total} concepts</span>`;
    moduleProgressList.appendChild(pill);
  });
}

function escapeHtml(text) {
  return String(text)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;');
}

function renderFormulas(formulas = []) {
  if (!formulas.length) return '';
  return `<section class="formula-list"><h5>Formulas / exact relationships</h5>${formulas.map(f => `<code>${escapeHtml(f)}</code>`).join('')}</section>`;
}

function renderMemorize(items = []) {
  if (!items.length) return '';
  return `<section class="memorize-list"><h5>Key points to memorize</h5><ul>${items.map(i => `<li>${escapeHtml(i)}</li>`).join('')}</ul></section>`;
}

function renderCode(blocks = []) {
  if (!blocks.length) return '';
  return blocks.map(block => `<section class="code-block"><h5>${escapeHtml(block.lang)} example</h5><pre><code>${escapeHtml(block.text)}</code></pre></section>`).join('');
}

function renderVisuals(visuals = []) {
  if (!visuals.length) return '';
  return `<div class="visual-grid">${visuals.map(v => `
    <figure class="figure-card">
      <button type="button" class="image-open" data-src="${escapeHtml(v.src)}" data-caption="${escapeHtml(v.caption)}">
        <img src="${escapeHtml(v.src)}" alt="${escapeHtml(v.caption)}" loading="lazy">
      </button>
      <figcaption>${escapeHtml(v.caption)}</figcaption>
    </figure>`).join('')}</div>`;
}

function renderQuiz(quiz = []) {
  if (!quiz.length) return '';
  return `<section class="quiz-box"><h5>Mini quiz</h5>${quiz.map((qa, idx) => `
    <div class="quiz-item">
      <p><strong>Q${idx + 1}.</strong> ${escapeHtml(qa.q)}</p>
      <button type="button" class="quiz-toggle">Show answer</button>
      <p class="quiz-answer">${escapeHtml(qa.a)}</p>
    </div>`).join('')}</section>`;
}

function renderConcept(concept) {
  const checked = Boolean(state.completed[concept.id]);
  return `<article class="concept-card ${checked ? 'completed' : ''}" data-concept-id="${escapeHtml(concept.id)}">
    <div class="concept-title-row">
      <h4>${escapeHtml(concept.title)}</h4>
      <label class="concept-check"><input type="checkbox" ${checked ? 'checked' : ''} data-check-id="${escapeHtml(concept.id)}"> Completed</label>
    </div>
    <div class="concept-body">${concept.body || ''}</div>
    ${renderFormulas(concept.formulas)}
    ${renderCode(concept.code)}
    ${renderMemorize(concept.memorize)}
    ${renderVisuals(concept.visuals)}
    ${renderQuiz(concept.quiz)}
  </article>`;
}

function renderLesson(lesson) {
  return `<section class="lesson-card">
    <div class="lesson-intro">
      <div class="lesson-title-row"><h3>${escapeHtml(lesson.title)}</h3></div>
      <p>${escapeHtml(lesson.summary)}</p>
      ${lesson.source ? `<span class="source-note">Source coverage: ${escapeHtml(lesson.source)}</span>` : ''}
      ${lesson.coverage ? `<div class="coverage"><strong>Coverage checklist:</strong><ul>${lesson.coverage.map(x => `<li>${escapeHtml(x)}</li>`).join('')}</ul></div>` : ''}
    </div>
    ${(lesson.concepts || []).map(renderConcept).join('')}
  </section>`;
}

function renderInventory() {
  const rows = courseData.meta.sourceFiles.map(f => `<tr><td>${escapeHtml(f.name)}</td><td>${escapeHtml(f.type)}</td><td>${f.count} ${escapeHtml(f.unit)}</td></tr>`).join('');
  return `<section class="lesson-card"><div class="lesson-intro"><h3>Source file inventory</h3><p>All uploaded course files were inspected and mapped into the study modules.</p></div><div class="inventory-table-wrap"><table><thead><tr><th>File</th><th>Type</th><th>Size checked</th></tr></thead><tbody>${rows}</tbody></table></div></section>`;
}

function renderModule() {
  const module = courseData.modules.find(m => m.id === state.activeModule) || courseData.modules[0];
  const stats = progressFor(module);
  const headerStats = module.id === 'overview'
    ? `<div class="stats-grid"><div class="stat-card"><strong>${courseData.meta.totals.files}</strong><span>course files</span></div><div class="stat-card"><strong>${courseData.meta.totals.pptxSlides}</strong><span>PowerPoint slides checked</span></div><div class="stat-card"><strong>${courseData.meta.totals.docxFiles}</strong><span>Word documents checked</span></div><div class="stat-card"><strong>${stats.percent}%</strong><span>topic progress</span></div></div>`
    : `<div class="stats-grid"><div class="stat-card"><strong>${module.lessons.length}</strong><span>lessons</span></div><div class="stat-card"><strong>${allConcepts(module).length}</strong><span>concepts</span></div><div class="stat-card"><strong>${stats.done}</strong><span>completed</span></div><div class="stat-card"><strong>${stats.percent}%</strong><span>topic progress</span></div></div>`;
  app.innerHTML = `<section class="module-header"><h2>${escapeHtml(module.title)}</h2><p>${escapeHtml(module.summary)}</p>${headerStats}</section>${module.lessons.map(renderLesson).join('')}${module.id === 'overview' ? renderInventory() : ''}`;
}

function bindEvents() {
  document.querySelectorAll('[data-check-id]').forEach(input => {
    input.addEventListener('change', event => {
      const id = event.target.dataset.checkId;
      if (event.target.checked) state.completed[id] = true;
      else delete state.completed[id];
      saveProgress();
      const card = event.target.closest('.concept-card');
      if (card) card.classList.toggle('completed', event.target.checked);
      renderProgress();
    });
  });

  document.querySelectorAll('.image-open').forEach(button => {
    button.addEventListener('click', () => {
      dialogImage.src = button.dataset.src;
      dialogCaption.textContent = button.dataset.caption;
      if (typeof dialog.showModal === 'function') dialog.showModal();
    });
  });

  document.querySelectorAll('.quiz-toggle').forEach(button => {
    button.addEventListener('click', () => {
      const answer = button.nextElementSibling;
      answer.classList.toggle('open');
      button.textContent = answer.classList.contains('open') ? 'Hide answer' : 'Show answer';
    });
  });
}

function render() {
  renderNav();
  renderProgress();
  renderModule();
  bindEvents();
}

themeToggle.addEventListener('click', () => {
  state.dark = !state.dark;
  document.body.classList.toggle('dark', state.dark);
  localStorage.setItem('cs496Theme', state.dark ? 'dark' : 'light');
  themeToggle.textContent = state.dark ? 'Light mode' : 'Dark mode';
});

resetProgress.addEventListener('click', () => {
  if (!confirm('Reset all completion progress?')) return;
  state.completed = {};
  saveProgress();
  render();
});

closeDialog.addEventListener('click', () => dialog.close());
dialog.addEventListener('click', event => {
  const rect = dialog.getBoundingClientRect();
  const outside = event.clientX < rect.left || event.clientX > rect.right || event.clientY < rect.top || event.clientY > rect.bottom;
  if (outside) dialog.close();
});

if (state.dark) document.body.classList.add('dark');
themeToggle.textContent = state.dark ? 'Light mode' : 'Dark mode';
render();
